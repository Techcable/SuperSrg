package net.techcable.supersrg.binary;

public class BinaryMappingsException extends Exception {
    public BinaryMappingsException(String message) {
        super(message);
    }
    public BinaryMappingsException(String message, Throwable cause) {
        super(message, cause);
    }

    public BinaryMappingsException(Throwable cause) {
        super(cause);
    }
}
